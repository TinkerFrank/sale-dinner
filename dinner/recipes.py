def normalize(text):
    import re
    return re.sub(r"\s+", " ", re.sub(r"[^a-z0-9\s]", " ", text.lower())).strip()


def parse_sale_items(raw):
    if not raw or not raw.strip():
        return []
    items = []
    for part in raw.replace(";", "\n").replace(",", "\n").split("\n"):
        item = part.strip()
        if item:
            import re
            item = re.sub(r"^\d+\.?\s*", "", item).strip()
            if item:
                items.append(item)
    return list(dict.fromkeys(items))


def ingredient_matches_sale(ingredient, sale_items):
    normalized_ingredient = normalize(ingredient)
    for sale in sale_items:
        normalized_sale = normalize(sale)
        if not normalized_sale:
            continue
        if (normalized_sale in normalized_ingredient or normalized_ingredient in normalized_sale):
            return True
        for word in normalized_ingredient.split():
            if len(word) > 3 and word in normalized_sale:
                return True
        for word in normalized_sale.split():
            if len(word) > 3 and word in normalized_ingredient:
                return True
    return False


def score_recipe(recipe, sale_items):
    matched, unmatched = [], []
    for ingredient in recipe["ingredients"]:
        if ingredient_matches_sale(ingredient, sale_items):
            matched.append(ingredient)
        else:
            unmatched.append(ingredient)
    match_ratio = len(matched) / len(recipe["ingredients"])
    score = len(matched) * 10 + match_ratio * 25
    return matched, unmatched, score, match_ratio


def suggest_recipes(sale_items_raw, limit=3, deals=None):
    from recipe_generator import generate_recipes_from_sales

    sale_items = parse_sale_items(sale_items_raw)
    if not sale_items:
        return {"saleItems": sale_items, "suggestions": []}

    suggestions = generate_recipes_from_sales(sale_items, limit=limit, deals=deals)
    source = "ai" if suggestions and suggestions[0].get("tags", []).count("ai") else "rules"
    return {"saleItems": sale_items, "suggestions": suggestions, "recipeSource": source}


